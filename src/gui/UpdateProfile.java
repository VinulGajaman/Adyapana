    private void jTextField2KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyTyped
        // TODO add your handling code here:
        String mobile = jTextField2.getText();
        String text = mobile + evt.getKeyChar();

        if (text.length() == 1) {
            if (!text.equals("0")) {
                evt.consume();
            }

        } else if (text.length() == 2) {
            if (!text.equals("07")) {
                evt.consume();
            }
        } else if (text.length() == 3) {
            if (!Pattern.compile("07[01245678]").matcher(text).matches()) {
                evt.consume();
            }
        } else if (text.length() <= 10) {
            if (!Pattern.compile("07[01245678][0-9]+").matcher(text).matches()) {
                evt.consume();
            }
        } else {
            evt.consume();
        }
    }//GEN-LAST:event_jTextField2KeyTyped

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String fName = jTextField4.getText();
        String lName = jTextField1.getText();
        String mobile = jTextField2.getText();
        String uName = jTextField3.getText();

        if (fName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter the First Name", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (lName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter the Last Name", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter the Mobile Number", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (!Pattern.compile("07[01245678][0-9]{7}").matcher(mobile).matches()) {
            JOptionPane.showMessageDialog(this, "Please Enter the Mobile Number", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            try {

                MySQL.iud("UPDATE `users` SET `firstName`='" + fName + "',`mobile`='" + mobile + "',`lastName`='" + lName + "' WHERE `username`='" + uName + "'");

                this.dispose();
                homeInstance.dispose();
                JOptionPane.showMessageDialog(this, "Profile Updated.", "Success", JOptionPane.INFORMATION_MESSAGE);
                Home newHomeInstance = new Home();
                newHomeInstance.updateProfile(fName, lName,uName);
                newHomeInstance.setVisible(true);

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

