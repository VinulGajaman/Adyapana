    public void loadUser(String userName) {

        try {
            this.userName = userName;
            ResultSet resultset = MySQL.search("SELECT * FROM `users` WHERE `username`='" + userName + "'");

            if (resultset.next()) {

                String uName = resultset.getString("username");
                String fName = resultset.getString("firstName");
                String lName = resultset.getString("lastName");
                String mobile = resultset.getString("mobile");
                String password = resultset.getString("password");

                jTextField4.setText(fName);
                jTextField1.setText(lName);
                jTextField2.setText(mobile);
                jTextField3.setText(uName);
                jTextField5.setText(password);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

